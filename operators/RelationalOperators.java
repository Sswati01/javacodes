/*Relational operators: equal to(==), not equal to(!=), greater than(>),
 lesser than(<), greater than equal to(>=), lesser than equal to(<=)*/
package operators;

public class RelationalOperators {
    public static void main (String args[]){
    int a=10;
    int b=10;
    System.out.println(a==b);
    System.out.println(a!=b);
    System.out.println(a>b);
    System.out.println(a<b);
    System.out.println(a>=b);
    System.out.println(a<=b);
    }
    
}
